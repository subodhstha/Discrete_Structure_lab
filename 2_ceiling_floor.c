// Program to implement the ceiling and floor functions
#include<stdio.h>
#include<math.h>
int main()
{
    printf("Floor of 2.2 is %f\n",floor(2.2));
    printf("Floor of -2.2 is %f\n",floor(-2.2));
    printf("Floor of 5.8 is %f\n",floor(5.8));
    printf("Celling of 2.2 is %f\n",ceil(2.2));
    printf("Celling of -2.2 is %f\n",ceil(-2.2));
    printf("Celling of 5.8 is %f\n",ceil(5.8));
}